export default async function AdminLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const supabase = await createClient();
  const { data: { user } } = await supabase.auth.getUser();

  if (!user) redirect("/login");

  const { data: profile } = await supabase
    .from("profiles")
    .select("role, display_name")
    .eq("id", user.id)
    .single();

  if (profile?.role !== "admin") redirect("/");

  return (
    <div className="flex min-h-[calc(100vh-4rem)]">
      {/* Sidebar */}
      <aside className="hidden w-64 shrink-0 border-r border-border bg-surface lg:block">
        <div className="flex h-full flex-col">
          <div className="border-b border-border px-6 py-4">
            <p className="text-xs font-medium uppercase tracking-wider text-muted">
              Admin Panel
            </p>
            <p className="mt-1 text-sm font-medium text-foreground">
              {profile?.display_name || user.email}
            </p>
          </div>

          <nav className="flex-1 space-y-1 px-3 py-4">
            {adminNav.map((item) => (
              <Link
                key={item.href}
                href={item.href}
                className="flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium text-muted transition-colors hover:bg-background hover:text-foreground"
              >
                {icons[item.icon]}
                {item.label}
              </Link>
            ))}
          </nav>

          <div className="border-t border-border px-3 py-4">
            <AdminLogout />
            <Link
              href="/"
              className="mt-2 flex items-center gap-3 rounded-lg px-3 py-2 text-sm text-muted transition-colors hover:bg-background hover:text-foreground"
            >
              &larr; Back to Site
            </Link>
          </div>
        </div>
      </aside>

      {/* Main content */}
      <div className="flex-1 bg-gray-50">
        <div className="p-6 lg:p-8">{children}</div>
      </div>
    </div>
  );
}
